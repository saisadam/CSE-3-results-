function loadResults(data) {
  const tbody = document.getElementById("resultsBody");
  tbody.innerHTML = "";
  data.forEach((student, index) => {
    const row = `<tr>
      <td>${student.roll}</td>
      <td>${student.name}</td>
      <td>${student.subject}</td>
      <td>${student.grade}</td>
      <td>${student.credits}</td>
      <td>${student.internal}</td>
      <td><button onclick="deleteStudent(${index})">Delete</button></td>
    </tr>`;
    tbody.innerHTML += row;
  });
}

function searchResults() {
  const input = document.getElementById("searchInput").value.toLowerCase();
  const filtered = students.filter(s =>
    s.name.toLowerCase().includes(input) || s.roll.includes(input)
  );
  loadResults(filtered);
}

function addStudent(event) {
  event.preventDefault();

  const roll = document.getElementById("roll").value.trim();
  const name = document.getElementById("name").value.trim();
  const subject = document.getElementById("subject").value.trim();
  const grade = document.getElementById("grade").value.trim();
  const credits = parseInt(document.getElementById("credits").value.trim());
  const internal = parseInt(document.getElementById("internal").value.trim());

  if (!roll || !name || !subject || !grade || isNaN(credits) || isNaN(internal)) {
    alert("Please fill in all fields correctly.");
    return;
  }

  students.push({ roll, name, subject, grade, credits, internal });
  loadResults(students);
  event.target.reset(); // clear form
}

function deleteStudent(index) {
  if (confirm("Are you sure you want to delete this result?")) {
    students.splice(index, 1);
    loadResults(students);
  }
}

loadResults(students);
